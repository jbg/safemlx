/// Reads and validates a Moshi MLX `config.json`.
///
/// When the file is absent, this returns [`ModelArgs::v0_1`], matching the
/// behavior used by `moshi_mlx` for the original Moshika/Moshiko releases.
pub fn get_model_args(model_dir: impl AsRef<Path>) -> Result<ModelArgs, Error> {
    let config_path = model_dir.as_ref().join("config.json");
    let args: ModelArgs = if config_path.exists() {
        serde_json::from_reader(std::fs::File::open(config_path)?)?
    } else {
        ModelArgs::v0_1()
    };
    args.validate()?;
    Ok(args)
}

/// Loads a dense or MLX affine-quantized Moshi checkpoint for token-only inference.
pub fn load_model(
    model_dir: impl AsRef<Path>,
    stream: &Stream,
    weights_stream: &Stream,
) -> Result<Model, Error> {
    let model_dir = model_dir.as_ref();
    let args = get_model_args(model_dir)?;
    let weights_name = args
        .moshi_name
        .clone()
        .unwrap_or_else(|| "model.safetensors".to_string());
    let mut model = Model::new(args, stream)?;
    let config = StrictLoadConfig::default();
    let mut report = StrictLoadReport::default();
    if weights_name == "model.safetensors"
        && model_dir.join("model.safetensors.index.json").exists()
    {
        load_safetensors_dir_strict(&mut model, model_dir, weights_stream, &config, &mut report)?;
    } else {
        load_safetensors_strict(
            &mut model,
            model_dir.join(&weights_name),
            weights_stream,
            &config,
            &mut report,
        )?;
    }
    report.finish(&model, &config)?;
    model.copy_to_stream(stream)?;
    Ok(model)
}

/// Loads a dense Moshi checkpoint while quantizing each matrix as it is read.
///
/// This is the experimental, no-conversion counterpart to
/// [`crate::quantization::quantize_checkpoint`]. Both paths call the same
/// affine tensor transform and produce the same in-memory parameter layout.
pub fn load_model_quantized(
    model_dir: impl AsRef<Path>,
    quantization: WeightQuantization,
    stream: &Stream,
    weights_stream: &Stream,
) -> Result<Model, Error> {
    let model_dir = model_dir.as_ref();
    let mut args = get_model_args(model_dir)?;
    if !crate::quantization::should_quantize_on_load("Moshi", args.quantization, quantization)? {
        return load_model(model_dir, stream, weights_stream);
    }
    args.quantization = Some(quantization);
    let weights_name = args
        .moshi_name
        .clone()
        .unwrap_or_else(|| "model.safetensors".to_string());
    let mut model = Model::new(args, stream)?;
    let config = StrictLoadConfig::default();
    let mut report = StrictLoadReport::default();
    if weights_name == "model.safetensors"
        && model_dir.join("model.safetensors.index.json").exists()
    {
        for file in crate::weights::safetensors_files(model_dir)? {
            load_safetensors_quantized_strict(
                &mut model,
                file,
                weights_stream,
                stream,
                quantization,
                &config,
                &mut report,
            )?;
        }
    } else {
        load_safetensors_quantized_strict(
            &mut model,
            model_dir.join(weights_name),
            weights_stream,
            stream,
            quantization,
            &config,
            &mut report,
        )?;
    }
    report.finish(&model, &config)?;
    model.copy_to_stream(stream)?;
    Ok(model)
}

/// Loads a PyTorch Moshi-family safetensors checkpoint into this model.
///
/// This supports the public Kyutai/NVIDIA PyTorch Moshi layout, where the
/// depth transformer stores per-codebook `weights_per_step` projections packed
/// along the output dimension. The loader rewrites those tensors into this
/// crate's explicit per-depth-slice parameter tree before strict validation.
pub fn load_pytorch_safetensors_model(
    args: ModelArgs,
    weights_path: impl AsRef<Path>,
    stream: &Stream,
    weights_stream: &Stream,
) -> Result<Model, Error> {
    let mut model = Model::new(args, stream)?;
    load_pytorch_safetensors_strict(&mut model, weights_path, weights_stream)?;
    model.copy_to_stream(stream)?;
    Ok(model)
}

/// Strict-loads one PyTorch Moshi-family safetensors file into an existing model.
pub fn load_pytorch_safetensors_strict(
    model: &mut Model,
    path: impl AsRef<Path>,
    weights_stream: &Stream,
) -> Result<(), Error> {
    load_pytorch_safetensors_files_strict(model, [path.as_ref()], weights_stream)
}

/// Strict-loads one or more PyTorch Moshi-family safetensors shards.
pub fn load_pytorch_safetensors_files_strict<P, I>(
    model: &mut Model,
    paths: I,
    weights_stream: &Stream,
) -> Result<(), Error>
where
    P: AsRef<Path>,
    I: IntoIterator<Item = P>,
{
    let config = StrictLoadConfig::default();
    let mut report = StrictLoadReport::default();
    let args = model.args.clone();
    for path in paths {
        let mut params = model.parameters_mut().flatten();
        for_each_safetensor_array(path.as_ref(), weights_stream, |source_key, value| {
            let transformed = transform_pytorch_tensor(&args, &source_key, value, weights_stream)?;
            for (key, value) in transformed {
                load_array_strict(&mut params, key, value, &config, &mut report);
            }
            Ok(())
        })?;
    }
    report.finish(model, &config)
}

/// Loads dense PyTorch Moshi-family shards while quantizing transformed tensors.
pub fn load_pytorch_safetensors_files_quantized_strict<P, I>(
    model: &mut Model,
    paths: I,
    weights_stream: &Stream,
    quantization_stream: &Stream,
    quantization: WeightQuantization,
) -> Result<(), Error>
where
    P: AsRef<Path>,
    I: IntoIterator<Item = P>,
{
    quantization.validate()?;
    let config = StrictLoadConfig::default();
    let mut report = StrictLoadReport::default();
    let args = model.args.clone();
    for path in paths {
        let mut params = model.parameters_mut().flatten();
        for_each_safetensor_array(path.as_ref(), weights_stream, |source_key, value| {
            let transformed = transform_pytorch_tensor(&args, &source_key, value, weights_stream)?;
            for (key, value) in transformed {
                load_array_quantized_strict(
                    &mut params,
                    key,
                    value,
                    quantization_stream,
                    quantization,
                    &config,
                    &mut report,
                )?;
            }
            Ok(())
        })?;
    }
    report.finish(model, &config)
}

fn transform_pytorch_tensor(
    args: &ModelArgs,
    key: &str,
    value: Array,
    stream: &Stream,
) -> Result<Vec<(String, Array)>, Error> {
    if let Some((index, suffix)) = parse_indexed_tensor(key, "emb") {
        return Ok(vec![(format!("audio_embs.{index}.{suffix}"), value)]);
    }
    if let Some((index, suffix)) = parse_indexed_tensor(key, "depformer_in") {
        return Ok(vec![(
            format!("depformer.slices.{index}.linear_in.{suffix}"),
            value,
        )]);
    }
    if let Some((index, suffix)) = parse_indexed_tensor(key, "linears") {
        return Ok(vec![(
            format!("depformer.slices.{index}.linear_out.{suffix}"),
            value,
        )]);
    }
    if let Some((index, suffix)) = parse_indexed_tensor(key, "depformer_emb") {
        return Ok(vec![(
            format!("depformer.slices.{}.emb.{suffix}", index + 1),
            value,
        )]);
    }
    if let Some(suffix) = key
        .strip_prefix("depformer_text_emb.")
        .and_then(tensor_suffix)
    {
        return Ok(vec![(format!("depformer.slices.0.emb.{suffix}"), value)]);
    }
    if key == "out_norm.alpha" {
        return Ok(vec![(
            "out_norm.weight".to_string(),
            squeeze_norm(value, stream)?,
        )]);
    }
    if let Some((layer, norm)) = parse_norm_alpha(key, "transformer.layers") {
        return Ok(vec![(
            format!("transformer.layers.{layer}.{norm}.weight"),
            squeeze_norm(value, stream)?,
        )]);
    }
    if let Some(rest) = key
        .strip_prefix("transformer.layers.")
        .and_then(|rest| rest.strip_suffix(".self_attn.in_proj_weight"))
    {
        return Ok(vec![(
            format!("transformer.layers.{rest}.self_attn.in_proj.weight"),
            value,
        )]);
    }
    if key.starts_with("text_emb.")
        || key.starts_with("text_linear.")
        || key.starts_with("transformer.layers.")
    {
        return Ok(vec![(key.to_string(), value)]);
    }

    if let Some((layer, norm)) = parse_norm_alpha(key, "depformer.layers") {
        let value = squeeze_norm(value, stream)?;
        let mut out = Vec::with_capacity(args.dep_q as usize);
        for slice in 0..args.dep_q {
            out.push((
                format!("depformer.slices.{slice}.transformer.layers.{layer}.{norm}.weight"),
                value.clone(),
            ));
        }
        return Ok(out);
    }
    if let Some((layer, suffix)) = parse_depformer_attention_weight(key) {
        let mut out = Vec::with_capacity(args.dep_q as usize);
        for slice in 0..args.dep_q {
            out.push((
                format!("depformer.slices.{slice}.transformer.layers.{layer}.self_attn.{suffix}"),
                split_first_axis(&value, args.dep_q, slice, stream)?,
            ));
        }
        return Ok(out);
    }
    if let Some((layer, slice, linear)) = parse_depformer_gating_weight(key) {
        return Ok(vec![(
            format!("depformer.slices.{slice}.transformer.layers.{layer}.gating.{linear}"),
            value,
        )]);
    }

    Ok(vec![(key.to_string(), value)])
}

fn squeeze_norm(value: Array, stream: &Stream) -> Result<Array, Exception> {
    let dim = value.dim(-1);
    value.reshape(&[dim], stream)
}

fn split_first_axis(
    value: &Array,
    parts: i32,
    index: i32,
    stream: &Stream,
) -> Result<Array, Exception> {
    let rows = value.dim(0);
    if rows % parts != 0 {
        return Err(Exception::custom(format!(
            "cannot split tensor with first dimension {rows} into {parts} parts"
        )));
    }
    let chunk = rows / parts;
    let start = index * chunk;
    let end = start + chunk;
    value.try_index_device((start..end, ..), stream)
}

fn tensor_suffix(suffix: &str) -> Option<&str> {
    matches!(suffix, "weight" | "scales" | "biases").then_some(suffix)
}

fn parse_indexed_tensor<'a>(key: &'a str, prefix: &str) -> Option<(i32, &'a str)> {
    let rest = key.strip_prefix(prefix)?.strip_prefix('.')?;
    let (index, suffix) = rest.split_once('.')?;
    Some((index.parse().ok()?, tensor_suffix(suffix)?))
}

fn parse_norm_alpha(key: &str, prefix: &str) -> Option<(i32, &'static str)> {
    let rest = key.strip_prefix(prefix)?.strip_prefix('.')?;
    let (layer, suffix) = rest.split_once('.')?;
    let norm = match suffix {
        "norm1.alpha" => "norm1",
        "norm2.alpha" => "norm2",
        _ => return None,
    };
    Some((layer.parse().ok()?, norm))
}

fn parse_depformer_attention_weight(key: &str) -> Option<(i32, &'static str)> {
    let rest = key.strip_prefix("depformer.layers.")?;
    let (layer, suffix) = rest.split_once('.')?;
    let suffix = match suffix {
        "self_attn.in_proj_weight" => "in_proj.weight",
        "self_attn.out_proj.weight" => "out_proj.weight",
        "self_attn.in_proj.weight" => "in_proj.weight",
        "self_attn.in_proj.scales" => "in_proj.scales",
        "self_attn.in_proj.biases" => "in_proj.biases",
        "self_attn.out_proj.scales" => "out_proj.scales",
        "self_attn.out_proj.biases" => "out_proj.biases",
        _ => return None,
    };
    Some((layer.parse().ok()?, suffix))
}

fn parse_depformer_gating_weight(key: &str) -> Option<(i32, i32, &'static str)> {
    let rest = key.strip_prefix("depformer.layers.")?;
    let (layer, rest) = rest.split_once(".gating.")?;
    let (slice, suffix) = rest.split_once('.')?;
    let linear = match suffix {
        "linear_in.weight" => "linear_in.weight",
        "linear_out.weight" => "linear_out.weight",
        "linear_in.scales" => "linear_in.scales",
        "linear_in.biases" => "linear_in.biases",
        "linear_out.scales" => "linear_out.scales",
        "linear_out.biases" => "linear_out.biases",
        _ => return None,
    };
    Some((layer.parse().ok()?, slice.parse().ok()?, linear))
}

