#[derive(Debug, Clone, ModuleParameters)]
struct MoshiMlp {
    #[param]
    linear_in: MoshiLinear,
    #[param]
    linear_out: MoshiLinear,
    compiled: CompiledUnaryWithStreamAndCaptures<MoshiMlpFn>,
}

impl MoshiMlp {
    fn unloaded(cfg: TransformerConfig, stream: &Stream) -> Result<Self, Exception> {
        let hidden = if cfg.feed_forward == 4 * cfg.dim {
            11 * cfg.dim / 4
        } else {
            2 * cfg.feed_forward / 3
        };
        Ok(Self {
            linear_in: MoshiLinear::unloaded(
                cfg.dim,
                2 * hidden,
                false,
                Dtype::Float32,
                cfg.quantization,
                stream,
            )?,
            linear_out: MoshiLinear::unloaded(
                hidden,
                cfg.dim,
                false,
                Dtype::Float32,
                cfg.quantization,
                stream,
            )?,
            compiled: compile_unary_with_stream_and_captures(moshi_mlp as MoshiMlpFn, [], false),
        })
    }

    fn forward(&mut self, x: &Array, stream: &Stream) -> Result<Array, Exception> {
        if self.linear_in.is_quantized() {
            let projected = self.linear_in.forward(x, stream)?;
            let mut parts = split(projected, 2, -1, stream)?;
            let gate = nn::silu(parts.remove(0), stream)?;
            return self
                .linear_out
                .forward(&gate.multiply(parts.remove(0), stream)?, stream);
        }
        if self.compiled.captures().is_empty() {
            self.compiled.set_captures([
                self.linear_in.weight_t(stream)?.clone(),
                self.linear_out.weight_t(stream)?.clone(),
            ]);
        }
        self.compiled.call(x, stream)
    }
}

fn moshi_mlp(x: &Array, captures: &[Array], stream: &Stream) -> Result<Array, Exception> {
    let projected = matmul(x, &captures[0], stream)?;
    let mut parts = split(projected, 2, -1, stream)?;
    let gate = nn::silu(parts.remove(0), stream)?;
    matmul(
        &gate.multiply(parts.remove(0), stream)?,
        &captures[1],
        stream,
    )
}

#[derive(Debug, Clone, ModuleParameters)]
struct MoshiTransformerLayer {
    #[param]
    norm1: nn::RmsNorm,
    #[param]
    norm2: nn::RmsNorm,
    #[param]
    self_attn: MoshiAttention,
    #[param]
    gating: MoshiMlp,
    compiled_pre: CompiledUnaryWithStreamAndCaptures<MoshiLayerPreFn>,
    compiled_post: CompiledBinaryWithStreamAndCaptures<MoshiLayerPostFn>,
}

impl MoshiTransformerLayer {
    fn unloaded(cfg: TransformerConfig, stream: &Stream) -> Result<Self, Exception> {
        Ok(Self {
            norm1: nn::RmsNorm::unloaded(cfg.dim, RMS_NORM_EPS, Dtype::Float32, stream)?,
            norm2: nn::RmsNorm::unloaded(cfg.dim, RMS_NORM_EPS, Dtype::Float32, stream)?,
            self_attn: MoshiAttention::unloaded(cfg, stream)?,
            gating: MoshiMlp::unloaded(cfg, stream)?,
            compiled_pre: compile_unary_with_stream_and_captures(
                moshi_layer_pre as MoshiLayerPreFn,
                [],
                false,
            ),
            compiled_post: compile_binary_with_stream_and_captures(
                moshi_layer_post as MoshiLayerPostFn,
                [],
                false,
            ),
        })
    }

    fn forward(
        &mut self,
        x: Array,
        cache: &mut ConcatKeyValueCache,
        stream: &Stream,
    ) -> Result<Array, Exception> {
        if self.self_attn.in_proj.is_quantized() {
            let norm1 = self.norm1.forward(&x, stream)?;
            let attention = self.self_attn.forward(&norm1, cache, stream)?;
            let post_attention = x.add(attention, stream)?;
            let norm2 = self.norm2.forward(&post_attention, stream)?;
            let mlp = self.gating.forward(&norm2, stream)?;
            return post_attention.add(mlp, stream);
        }
        if self.compiled_pre.captures().is_empty() {
            let in_proj = self.self_attn.in_proj.weight_t(stream)?.clone();
            self.compiled_pre
                .set_captures([self.norm1.weight.value.clone(), in_proj]);
        }
        if self.compiled_post.captures().is_empty() {
            let out_proj = self.self_attn.out_proj.weight_t(stream)?.clone();
            let mlp_in = self.gating.linear_in.weight_t(stream)?.clone();
            let mlp_out = self.gating.linear_out.weight_t(stream)?.clone();
            self.compiled_post.set_captures([
                out_proj,
                self.norm2.weight.value.clone(),
                mlp_in,
                mlp_out,
            ]);
        }
        let qkv = self.compiled_pre.call(&x, stream)?;
        let attended = self.self_attn.attend_projected(qkv, cache, stream)?;
        self.compiled_post.call(&x, &attended, stream)
    }

    fn forward_traced(
        &mut self,
        x: Array,
        cache: &mut ConcatKeyValueCache,
        stream: &Stream,
    ) -> Result<TemporalLayerTrace, Exception> {
        let norm1 = self.norm1.forward(&x, stream)?;
        let attention_trace = self.self_attn.forward_traced(&norm1, cache, stream)?;
        let post_attention = x.add(&attention_trace.output, stream)?;
        let norm2 = self.norm2.forward(&post_attention, stream)?;
        let mlp = self.gating.forward(&norm2, stream)?;
        let output = post_attention.add(&mlp, stream)?;
        Ok(TemporalLayerTrace {
            norm1,
            attention_in_proj: attention_trace.in_proj,
            attention_queries: attention_trace.queries,
            attention_keys: attention_trace.keys,
            attention_values: attention_trace.values,
            attention_attended: attention_trace.attended,
            attention: attention_trace.output,
            post_attention,
            norm2,
            mlp,
            output,
        })
    }
}

fn moshi_layer_pre(x: &Array, captures: &[Array], stream: &Stream) -> Result<Array, Exception> {
    let normalized = safemlx::fast::rms_norm(x, &captures[0], RMS_NORM_EPS, stream)?;
    matmul(&normalized, &captures[1], stream)
}

fn moshi_layer_post(
    (residual, attended): (&Array, &Array),
    captures: &[Array],
    stream: &Stream,
) -> Result<Array, Exception> {
    let x = residual.add(matmul(attended, &captures[0], stream)?, stream)?;
    let normalized = safemlx::fast::rms_norm(&x, &captures[1], RMS_NORM_EPS, stream)?;
    let projected = matmul(&normalized, &captures[2], stream)?;
    let mut parts = split(projected, 2, -1, stream)?;
    let gate = nn::silu(parts.remove(0), stream)?;
    let mlp = matmul(
        &gate.multiply(parts.remove(0), stream)?,
        &captures[3],
        stream,
    )?;
    x.add(mlp, stream)
}

#[derive(Debug, Clone, ModuleParameters)]
struct MoshiTransformer {
    #[param]
    layers: Vec<MoshiTransformerLayer>,
}

impl MoshiTransformer {
    fn unloaded(cfg: TransformerConfig, stream: &Stream) -> Result<Self, Exception> {
        let layers = (0..cfg.num_layers)
            .map(|_| MoshiTransformerLayer::unloaded(cfg, stream))
            .collect::<Result<_, _>>()?;
        Ok(Self { layers })
    }

    fn forward(
        &mut self,
        mut x: Array,
        cache: &mut [ConcatKeyValueCache],
        stream: &Stream,
    ) -> Result<Array, Exception> {
        if cache.len() != self.layers.len() {
            return Err(Exception::custom("invalid Moshi transformer cache length"));
        }
        for (layer, cache) in self.layers.iter_mut().zip(cache) {
            x = layer.forward(x, cache, stream)?;
        }
        Ok(x)
    }

    fn forward_traced(
        &mut self,
        mut x: Array,
        cache: &mut [ConcatKeyValueCache],
        stream: &Stream,
    ) -> Result<(Array, Vec<TemporalLayerTrace>), Exception> {
        if cache.len() != self.layers.len() {
            return Err(Exception::custom("invalid Moshi transformer cache length"));
        }
        let mut layer_traces = Vec::with_capacity(self.layers.len());
        for (layer, cache) in self.layers.iter_mut().zip(cache) {
            let trace = layer.forward_traced(x, cache, stream)?;
            x = trace.output.clone();
            layer_traces.push(trace);
        }
        Ok((x, layer_traces))
    }
}

#[derive(Debug, Clone, ModuleParameters)]
struct DepFormerSlice {
    #[param]
    emb: ScaledEmbedding,
    #[param]
    linear_in: MoshiLinear,
    #[param]
    linear_out: MoshiLinear,
    #[param]
    transformer: MoshiTransformer,
}

impl DepFormerSlice {
    fn unloaded(
        input_vocab: i32,
        main_dim: i32,
        cfg: TransformerConfig,
        audio_vocab: i32,
        stream: &Stream,
    ) -> Result<Self, Exception> {
        Ok(Self {
            emb: ScaledEmbedding::unloaded(input_vocab, cfg.dim, cfg.quantization, stream)?,
            linear_in: MoshiLinear::unloaded(
                main_dim,
                cfg.dim,
                false,
                Dtype::Float32,
                cfg.quantization,
                stream,
            )?,
            linear_out: MoshiLinear::unloaded(
                cfg.dim,
                audio_vocab - 1,
                false,
                Dtype::Float32,
                cfg.quantization,
                stream,
            )?,
            transformer: MoshiTransformer::unloaded(cfg, stream)?,
        })
    }
}

#[derive(Debug, Clone, ModuleParameters)]
struct DepFormer {
    #[param]
    slices: Vec<DepFormerSlice>,
}

/// Stateful caches for temporal and within-frame depth inference.
#[derive(Debug, Clone)]
pub struct MoshiCache {
    temporal: Vec<ConcatKeyValueCache>,
    depth: Vec<ConcatKeyValueCache>,
}

impl MoshiCache {
    /// Clears both temporal history and the current depth frame.
    pub fn reset(&mut self) {
        self.temporal.iter_mut().for_each(|cache| cache.clear());
        self.depth.iter_mut().for_each(|cache| cache.clear());
    }

    fn reset_depth(&mut self) {
        self.depth.iter_mut().for_each(|cache| cache.clear());
    }
}

